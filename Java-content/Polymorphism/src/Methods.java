public class Methods {
    public void show(){
        System.out.println("This is my first behaviour");
    }
    public void show(int num){
        System.out.println("This is my second behaviour which takes integer values");
    }
    public void show(double num){
        System.out.println("This is my third behaviour where i accept real numbers");
    }
    public void show(boolean value){
        System.out.println("This is my fourth behaviour i accept boolean values");
    }
}
